//main method is mandatory in java
public class Main {
    // class name only => File name
    // for class, we use PascalCase
    public static void main(String[] args) {


        // public static void main => is a default function in java

        /*
          void means this function returns nothing
          static means this function can directly run without having been in an object

          for function , we use camelCase
         */

        // semicolon mandatory
        System.out.print("Sum of these numbers is : ");
        //"println" method prints a statement as a block element [add a new line]
        //where "print" method prints a statement as an inline element

        int num1=5;
        int num2=6;
        int num3=7;
        int sum=num1+num2+num3;
        System.out.println(sum);


    }
}

